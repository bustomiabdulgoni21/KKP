    private void tabeldatakelasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabeldatakelasMouseClicked
        // TODO add your handling code here:
        try {
            int row = tabeldatakelas.getSelectedRow();
            String tabel_klik = (tabeldatakelas.getModel().getValueAt(row, 0).toString());
            java.sql.Statement stm = conn.createStatement();
            java.sql.ResultSet sql = stm.executeQuery("select * from tabeldatakelas where idkelas='"+tabel_klik+"'");
            if(sql.next()) {
                String jns = sql.getString("idkelas");
                txtidkelas.setText(jns);
                String har = sql.getString("kelas");
                txtkelas.setText(har);
                
            }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Tidak ada data yang dipilih");
        }
    }//GEN-LAST:event_tabeldatakelasMouseClicked

