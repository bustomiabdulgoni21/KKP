     protected void datatable() {
        Object[] Baris = {"Id. Kelas","Kelas"};
        tabmode = new DefaultTableModel(null, Baris);
        tabeldatakelas.setModel(tabmode);
        String sql = "select * from tabeldatakelas";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String b = hasil.getString("idkelas");
                String c = hasil.getString("kelas");
                String[] data = {b,c};
                tabmode.addRow(data);
            }
            }catch (Exception e) {
                
            }
    }
     
      public void auto() {
           try {
               String sql = "select max(right(idkelas,3)) from tabeldatakelas";
               java.sql.Statement stat = conn.createStatement();
               ResultSet hasil = stat.executeQuery(sql);
               while(hasil.next()) {
                   if(hasil.first()==false) {
                       txtidkelas.setText("KLS001");
                   } else {
                       hasil.last();
                       int code = hasil.getInt(1)+1;
                       String Nomor = String.valueOf(code);
                       int noLong = Nomor.length();
                       
                       for (int a=0; a<3-noLong; a++){
                           Nomor = "0" + Nomor;
                       }
                       txtidkelas.setText("KLS-"+ Nomor);
                   }
               }
           }catch (Exception e){
               
           }
       }
      
