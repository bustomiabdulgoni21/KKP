    protected void datatable() {
        Object[] Baris = {"No Pembayaran", "Tanggal Pembayaran", "NIS","Nama","Kelas", "Jenis Pembayaran", "Biaya","kurang", "Total", "Keterangan"};
        tabmode = new DefaultTableModel(null, Baris);
        tabellaporanpembayaran.setModel(tabmode);
        String sql = "select * from pembayaran";
        try {
            java.sql.Statement stat = conn.createStatement();
            ResultSet hasil = stat.executeQuery(sql);
            while(hasil.next()){
                String no = hasil.getString("no_pemb");
                String tgl = hasil.getString("tgl_pem");
                String Nis = hasil.getString("nis");
                 String Nii = hasil.getString("nama");
                 String kel = hasil.getString("kelas");
                // String a = hasil.getString("angkatan");
                  String Niss = hasil.getString("jns_pem");
                   String Nisss = hasil.getString("biaya");
                    String Nissss = hasil.getString("total");
                    String z = hasil.getString("kurang");
                    String Niis = hasil.getString("ket");
                String[] data = {no,tgl,Nis,Nii,kel,Niss,Nisss,Nissss,z,Niis};
                tabmode.addRow(data);
            }
            }catch (Exception e) {
            }
            }
    
